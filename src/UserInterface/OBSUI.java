    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try {
            ogretmenTablo1.getTabloModel().setRowCount(0);
            LinkedList<Ogretmen> tumOgretmenler = sqlKullaniciIslemi.tumOgretmenleriBul(secilenOkulId);
            Integer toplamOgretmenSayisi = tumOgretmenler.size();
            
            for(int i=0;i<toplamOgretmenSayisi;i++){
                
               ogretmenTablo1.tabloDoldur(tumOgretmenler.get(i));
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(OBSUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        Integer secilenSatir = jTable_OgretmenTablo2.getSelectedRow();
        Integer secilenOgretmenId;
        boolean sonuc = false;
        
        if(jList_Okullar.isSelectionEmpty() || secilenSatir == -1){
            
            JOptionPane.showMessageDialog(this, "Okul ve öğretmen seçmediniz!!!");
            
        }else{
            
            secilenOgretmenId = (Integer) jTable_OgretmenTablo2.getValueAt(secilenSatir, 0);
            sonuc = sqlKullaniciIslemi.ogretmenAtamasiYap(secilenOkulId, secilenOgretmenId);
            
            if(sonuc){
                
                JOptionPane.showMessageDialog(this, "Öğretmen ataması başarılı.");
            }else{
                
                JOptionPane.showMessageDialog(this, "Atama yapılamadı!!!");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

