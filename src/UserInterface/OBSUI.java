    public void kullaniciRoluBelirle(Kullanici kullanici){
        
       switch (kullanici.getRole()) {
            case "Admin":
                admin = (Admin) kullanici;
           
                break;
            case "Ogrenci":
                ogrenci = (Ogrenci) kullanici;
                break;
            case "Ogretmen":
                ogretmen = (Ogretmen) kullanici;
                break;
            case "Mudur":
                okulMuduru = (OkulMuduru) kullanici;
                break;
            default:
                kullanici = null;
                break;
        }  
        
        
        
    }

